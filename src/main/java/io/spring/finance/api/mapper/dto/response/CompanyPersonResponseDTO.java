package io.spring.finance.api.mapper.dto.response;

import java.util.List;

public class CompanyPersonResponseDTO {

	private Long id;
	private String name;
	private String document;
	private String typePerson;
	private List<ContactResponseDTO> contacts;
	private String cep;
	private String district;
	private String street;
	private String city;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDocument() {
		return document;
	}

	public void setDocument(String document) {
		this.document = document;
	}

	public String getTypePerson() {
		return typePerson;
	}

	public void setTypePerson(String typePerson) {
		this.typePerson = typePerson;
	}

	public List<ContactResponseDTO> getContacts() {
		return contacts;
	}

	public void setContacts(List<ContactResponseDTO> contacts) {
		this.contacts = contacts;
	}

	public String getCep() {
		return cep;
	}

	public void setCep(String cep) {
		this.cep = cep;
	}

	public String getDistrict() {
		return district;
	}

	public void setDistrict(String district) {
		this.district = district;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

}
